    def get_news(self, meshes, mesh_size):
        list_meshes = []
        for mesh in meshes:
            meshcode = MeshCode(mesh)
            if mesh_size == "1km":
                news = meshcode.wgs_1km_news()
            elif mesh_size == "500m":
                news = meshcode.wgs_500m_news()
            elif mesh_size == "250m":
                news = meshcode.wgs_250m_news()
            elif mesh_size == "125m":
                news = meshcode.wgs_125m_news()
            elif mesh_size == "100m":
                news = meshcode.wgs_100m_news()
            elif mesh_size == "10m":
                news = meshcode.wgs_10m_news()
            elif mesh_size == "1m":
                news = meshcode.wgs_1m_news()

            list_meshes.append({"meshcode": mesh, "news":news})

        return list_meshes


    def show_data(self, mydata, layer_name):

        layer_bs = self.iface.activeLayer()
        crs_id = layer_bs.crs().authid()
        dest_crs = QgsCoordinateReferenceSystem(crs_id)

        layer = QgsVectorLayer('Polygon', layer_name, 'memory')
        pr = layer.dataProvider()

        # new fieldの追加
        new_fields = [QgsField("メッシュコード", QVariant.String), QgsField("面積(km2)", QVariant.String),
                      QgsField("緯度距離（北側）(m)", QVariant.String),
                      QgsField("緯度距離（南側）(m)", QVariant.String),
                      QgsField("経度距離(m)", QVariant.String)]
        pr.addAttributes(new_fields)
        layer.updateFields()

        for row in mydata:

            # polygon追加
            poly = QgsFeature()
            geom = QgsGeometry.fromWkt(row["WKT"])
            poly.setGeometry(geom)

            # new fieldに
            poly.setAttributes([row["meshcode"],
                                round(row["area"]/1000000,2),
                                round(row["latitude_North"],2),
                                round(row["latitude_South"],2),
                                round(row["longitude"],2)])

            # new featureを追加
            pr.addFeatures([poly])

        layer.updateExtents()

        # 現在のシンボルを取得
        symbol = layer.renderer().symbol()

        # 塗りつぶし色を取得
        current_color = symbol.color()

        # 新しい色（透明度を下げる）
        # QColor(R, G, B, Alpha)  Alpha=0〜255
        new_color = QColor(current_color.red(),
                           current_color.green(),
                           current_color.blue(),
                           100)  # 100 は半透明

        # 色を設定
        symbol.setColor(new_color)

        # レイヤーを再描画
        layer.triggerRepaint()



        QgsProject.instance().addMapLayers([layer])

        pass



    def draw_meshes(self):
        # comboboxの値を取得
        mesh_size = self.dlg.comboBox.currentText()
        codes_in_area = self.get_code(mesh_size)
        coordinates_in_area = self.get_coordinate(codes_in_area, mesh_size)
        area_coordinates = Coordinate.get_area_news(x_min=int(coordinates_in_area["bottom_left_coordinate"]["x"]),
                                                    x_max=int(coordinates_in_area["bottom_right_coordinate"]["x"]),
                                                    y_min=int(coordinates_in_area["bottom_left_coordinate"]["y"]),
                                                    y_max=int(coordinates_in_area["top_left_coordinate"]["y"]))
        area_meshes = self.get_meshes(area_coordinates, mesh_size)
        mesh_news = self.get_news(area_meshes, mesh_size)

        mylist = []
        for item in mesh_news:
            wkt = LatitudeAndLongitude.dict2wkt(North=item["news"]["North"], South=item["news"]["South"],
                                                East=item["news"]["East"], West=item["news"]["West"])
            distance_latitude_North = LatitudeAndLongitude.distance(latitude_a=item["news"]["North"],
                                                                    longitude_a=item["news"]["West"],
                                                                    latitude_b=item["news"]["North"],
                                                                    longitude_b=item["news"]["East"])
            distance_latitude_South = LatitudeAndLongitude.distance(latitude_a=item["news"]["South"],
                                                                    longitude_a=item["news"]["West"],
                                                                    latitude_b=item["news"]["South"],
                                                                    longitude_b=item["news"]["East"])
            longitude = LatitudeAndLongitude.distance(latitude_a=item["news"]["North"],
                                                                    longitude_a=item["news"]["East"],
                                                                    latitude_b=item["news"]["South"],
                                                                    longitude_b=item["news"]["East"])
            area = LatitudeAndLongitude.rectangle_area(latitude_a=item["news"]["North"],
                                                       longitude_a=item["news"]["East"],
                                                       latitude_b=item["news"]["North"],
                                                       longitude_b=item["news"]["West"],
                                                       latitude_c=item["news"]["South"],
                                                       longitude_c=item["news"]["West"],
                                                       latitude_d=item["news"]["South"],
                                                       longitude_d=item["news"]["East"])
            mylist.append({"meshcode":item["meshcode"],
                           "WKT": wkt,
                           "area":area,
                           "latitude_North":distance_latitude_North,
                           "latitude_South":distance_latitude_South,
                           "longitude":longitude})

        self.show_data(mylist, mesh_size)





