    def count_meshes(self):
        # comboboxの値を取得
        mesh_size = self.dlg.comboBox.currentText()
        codes_in_area = self.get_code(mesh_size)

        coordinates_in_area = self.get_coordinate(codes_in_area, mesh_size)
        x_length = int(coordinates_in_area["bottom_right_coordinate"]["x"]) \
                   - int(coordinates_in_area["bottom_left_coordinate"]["x"]) + 1
        y_length = int(coordinates_in_area["top_left_coordinate"]["y"]) \
                   - int(coordinates_in_area["bottom_left_coordinate"]["y"]) + 1
        mesh_number = x_length * y_length

        # mesh_numberをsetTextする。
        self.dlg.textEdit.setText(str(mesh_number))

    def get_meshes(self, area_coordinates, mesh_size):
        area_meshes = []
