    def get_latitudeandlongitude(self):
        # 現在のキャンバスの表示範囲（地図座標系）
        extent = self.iface.mapCanvas().extent()

        ######################################################
        print("CRS:", self.iface.activeLayer().crs().authid())
        ######################################################

        # EPSG:3857 -> EPSG:4326

        # 現在のプロジェクトの CRS（例: EPSG:qgis ラスタレイヤ 画面表示されている四隅の緯度経度をpythonで求める方法3857 など）
        src_crs = self.iface.mapCanvas().mapSettings().destinationCrs()

        # 緯度経度（EPSG:4326）に変換するための CRS
        dest_crs = QgsCoordinateReferenceSystem("EPSG:4326")

        # 座標変換オブジェクトを作成
        transform = QgsCoordinateTransform(src_crs, dest_crs, QgsProject.instance())

        # 四隅の座標を変換
        top_left = transform.transform(extent.xMinimum(), extent.yMaximum())
        top_right = transform.transform(extent.xMaximum(), extent.yMaximum())
        bottom_left = transform.transform(extent.xMinimum(), extent.yMinimum())
        bottom_right = transform.transform(extent.xMaximum(), extent.yMinimum())

        return {"top_left": [top_left.y(), top_left.x()],
                "top_right": [top_right.y(), top_right.x()],
                "bottom_left": [bottom_left.y(), bottom_left.x()],
                "bottom_right": [bottom_right.y(), bottom_right.x()]}


